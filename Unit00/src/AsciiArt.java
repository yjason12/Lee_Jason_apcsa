public class AsciiArt
{
	public static void main ( String[] args )
	{
		System.out.println("Jason Lee         1/31/19 \n\n" );
		System.out.println("This is my animal! A cat!" );
		System.out.println("\n\n\n\n" );

		System.out.println("                                " );
		System.out.println("               ^  ^             " );
		System.out.println("              (owo)              " );
		System.out.println("                |       " );
		System.out.println("                |");
		System.out.println("               \\|/ ");
		System.out.println("                |");
		System.out.println("                |");
		System.out.println("                |");
		System.out.println("                |");
		System.out.println("                /\\");
		System.out.println(" \n\n\n\nHelpFul Hints" );
		System.out.println("\\\\ draws one backslash on the screen!\n" );
		System.out.println("\\\" draws one double quote on the screen!\n" );
		System.out.println("\\\' draws one single quote on the screen!\n" ); 
	}
}

